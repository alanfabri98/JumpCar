
public class GameState 
{
    int numItemsCollected;
    int lifes;
    int numItems;
    int points;
    public void GameState(int numItemsCollected, int lifes,int numItems,int points)
    {
        this.numItemsCollected = numItemsCollected;
        this.lifes = lifes;
        this.numItems = numItems;
        this.points = points;
    }

}
